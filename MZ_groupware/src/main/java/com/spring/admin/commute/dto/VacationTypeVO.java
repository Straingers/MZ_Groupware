package com.spring.admin.commute.dto;

public class VacationTypeVO {
	private String vacCode;
	private String vacName;
	private String vacDays;
	private String isUse;
	private String isDel;
	  
	public String getVacCode() {
		return vacCode;
	}
	public void setVacCode(String vacCode) {
		this.vacCode = vacCode;
	}
	public String getVacName() {
		return vacName;
	}
	public void setVacName(String vacName) {
		this.vacName = vacName;
	}
	public String getVacDays() {
		return vacDays;
	}
	public void setVacDays(String vacDays) {
		this.vacDays = vacDays;
	}
	public String getIsUse() {
		return isUse;
	}
	public void setIsUse(String isUse) {
		this.isUse = isUse;
	}
	public String getIsDel() {
		return isDel;
	}
	public void setIsDel(String isDel) {
		this.isDel = isDel;
	}
	
	@Override
	public String toString() {
		return "VacationTypeVO [vacCode=" + vacCode + ", vacName=" + vacName + ", vacDays=" + vacDays + ", isUse="
				+ isUse + ", isDel=" + isDel + "]";
	}
	 
}
