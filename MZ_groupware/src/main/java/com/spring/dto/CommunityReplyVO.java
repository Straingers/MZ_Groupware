package com.spring.dto;

import java.util.Date;

public class CommunityReplyVO {

	private int replyNo;		
	private int commNo;		
	private String replytext;	
	private String replyer;		
	private Date regdate;	
	private String isDel;
	private String communityCode;
	
	private String replyerPhoto;
	
	
	
	public String getReplyerPhoto() {
		return replyerPhoto;
	}
	public void setReplyerPhoto(String replyerPhoto) {
		this.replyerPhoto = replyerPhoto;
	}
	public Date getRegdate() {
		return regdate;
	}
	public void setRegdate(Date regdate) {
		this.regdate = regdate;
	}
	public int getReplyNo() {
		return replyNo;
	}
	public void setReplyNo(int replyNo) {	
		this.replyNo = replyNo;
	}
	public int getCommNo() {
		return commNo;
	}
	public void setCommNo(int commNo) {
		this.commNo = commNo;
	}
	public String getReplytext() {
		return replytext;
	}
	public void setReplytext(String replytext) {
		this.replytext = replytext;
	}
	public String getReplyer() {
		return replyer;
	}
	public void setReplyer(String replyer) {
		this.replyer = replyer;
	}
	
	public String getIsDel() {
		return isDel;
	}
	public void setIsDel(String isDel) {
		this.isDel = isDel;
	}
	public String getCommunityCode() {
		return communityCode;
	}
	public void setCommunityCode(String communityCode) {
		this.communityCode = communityCode;
	}
	
	@Override
	public String toString() {
		return "CommunityReplyVO [replyNo=" + replyNo + ", commNo=" + commNo + ", replytext=" + replytext + ", replyer="
				+ replyer + ", replyRegdate=" + regdate + ", isDel=" + isDel + ", communityCode=" + communityCode
				+ "]";
	}
}
