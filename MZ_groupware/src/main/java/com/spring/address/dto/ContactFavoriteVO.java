package com.spring.address.dto;

public class ContactFavoriteVO {
	private String favoriteEmpNo;
	private String empNo;

	public String getFavoriteEmpNo() {
		return favoriteEmpNo;
	}
	public void setFavoriteEmpNo(String favoriteEmpNo) {
		this.favoriteEmpNo = favoriteEmpNo;
	}
	public String getEmpNo() {
		return empNo;
	}
	public void setEmpNo(String empNo) {
		this.empNo = empNo;
	}
	
	@Override
	public String toString() {
		return "ContactFavoriteVO [favoriteEmpNo=" + favoriteEmpNo + ", empNo=" + empNo + "]";
	}
}
